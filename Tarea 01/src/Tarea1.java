public class Tarea1 {
    public static void main(String[] args) {
        int x = 200, y = 111;
        System.out.println("X vale: " + x);
        System.out.println("Y vale: " + y);
        System.out.println("X + y es igual a: " + (x + y));
        System.out.println("X - y es igual a: " + (x - y));
        System.out.println("X / y es igual a: " + (x / y));
        System.out.println("X * y es igual a: " + (x * y));
    }
}